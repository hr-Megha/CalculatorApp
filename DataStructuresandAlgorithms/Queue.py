# What is Queue ?
# Linear Data structure 
# Follows FIFO - First In First Out
# Insertion can take place from the rear end 
# Deletion can take place from the front end
#  4 Major operations
#   1.enqueue(ele) - used to insert element at top
#   2. dequeue() - removes the top element from queue
#   3.peekfirst() - to get the first element of queue 
#   4.peeklast() - to get the last element of queue

# All operation works in constant time that is, 0(1)

# Applications of Queue
 # 1. Scheduling 
 # 2. Maintaining playlist 
 # 3. Interrupt handling 